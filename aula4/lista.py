# definir uma lista com 3 elementos
lista = [10, 20, 30]
print(lista)

# acessar por índice
print(lista[0])

# alterar por índice
lista[1]= 40
print(lista)

# adicionar
lista.append(40)
print(lista)

# adicionar por índice
lista.insert(2, 25)
print(lista)

# remover por valor
lista.remove(30)
print(lista)

# remover por índice
del lista[2]
print(lista)



